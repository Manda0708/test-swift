func getMax(nombre1: Int, nombre2: Int) -> Any {
    if nombre1 > nombre2 {
        return nombre1
    }
    return nombre2
}
var a: Int = 10
var b: Int = 2
print(getMax(nombre1: a, nombre2: b))

